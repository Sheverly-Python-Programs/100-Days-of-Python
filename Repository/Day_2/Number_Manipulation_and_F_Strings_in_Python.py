print(round(8 / 3)) #The round function is used to round off numbers
# The final answer is 3

print(round(8 / 3, 2)) # The ", 2" means that you want to get the number of the decimal places
# The final answer is 2.67

print(8 // 3) # The "//" stands for floor division. It will automatically convert a floating point number into an integer.
# The answer is 2

